        <h1 id="brand-guidelines">3. Brand Guidelines</h1>
        <small>Task</small>
        <h4>Develop Brand Guidelines</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>4&thinsp;&ndash;&thinsp;6 hours</p>
        <small>Description</small>
        <p>Consistency is essential for a memorable brand.</p>
        <p>The Guidelines document makes your Brand accessible to all members of your organization as well as external partners and vendors. It also provides a convenient framework for updating the Guidelines as the company grows and the brand evolves.</p>
        <p>The Brand Guidelines could be presented as a digital slideshow, or in a PDF document, you might also want to print the manual; However, having the Brand Guidelines in a web platform would have significant more accessibility.</p>
        <p><a href="https://www.frontify.com/en/" target="_blank">Frontify</a> and <a href="https://www.lingoapp.com/" target="_blank">Lingo</a> offer a paid web platform to manage you Brand's Guidelines and Assets. Alternatively, a custom web page could be developed for the document.</p>
        <p>The Brand Guidelines document can include the key insights from the Brand Research and Strategy; However, mind that making your research openly available online would give an advantage to your competitor's research.</p>
