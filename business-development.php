        <h3 id="industry-analysis">1.3. Industry Analysis</h3>
        <small>Task</small>
        <h4>List the Industry leaders and direct competitors</h4>
        <small>Role</small>
        <p>Marketing Analyst</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;2 hours</p>
        <small>Description</small>
        <p>By now, based on the Business Analysis, we know the Industry and Niche it belongs to.</p>
        <p>A quick Google search can help us identify the industry leaders worldwide, and by adding the location of the specific markets of the business to the Google search we can also find direct competitors operating in the same area.</p>
        <p>Different tools can also be used to better understand the industry and Target Public, like (1) Who's using and following related social media hashtags, (2) Who's following your other brands from your industry on social media, and (3) Analytics tools (like <a href="https://www.similarweb.com" target="_blank">SimilarWeb</a>, <a href="https://www.alexa.com/" target="_blank">Alexa</a>, and <a href="https://www.quantcast.com/" target="_blank">Quantcast</a>).</p>
        <p>For more technical, complex industries, you may browse research publications on <a href="https://www.researchgate.net" target="_blank">ResearchGate</a> or <a href="https://www.academia.edu" target="_blank">Academia.edu</a>.</p>
        <p>Make a list of both the industry leaders and direct competitors considering (1) which traits are common in the industry's brands, (2) which traits can be used to differentiate the business' Brand over its competitors, and (3) which brand is closer to the business' aspirations.</p>
        <p>While you survey the industry it would be useful to additionally list Keywords that you might find relatable to the industry.</p>
