        <h3 id="target-public-analysis">1.2. Target Public Analysis</h3>
        <small>Task</small>
        <h4>Identify the Target Public</h4>
        <small>Role</small>
        <p>Marketing Analyst</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;2 hours</p>
        <small>Description</small>
        <p>By understanding the business and surveying the industry we can already intuitively narrow down some demographics of what an ideal customer should be like.</p>
        <p>Furthermore, especially if you're designing a Brand Identity for a foreign market, it's very important to understand the cultural context. You can start by surveying the country's cultural background and demographics on <a href="https://wikipedia.org" target="_blank">Wikipedia</a> or on <a href="https://www.youtube.com/user/GeographyNow" target="_blank">Geography Now!</a>.</p>
        <p>However, it is possible for a Brand to have multiple Public Groups which serve different purposes. For instance, a creative agency might have a Public Group (A) which includes decision makers of other businesses needing creative services, and (B) people from the creative industry who would be interested in the content published by the Brand.</p>
        <p>At this point we must identify the different possible Public Groups, breakdown their corresponding demographic characteristics, and make <strong>User Personas</strong> stereotyping the ideal Public member of each group.</p>
        <p>Each User Persona can include traits like:</p>
        <div class="indent">
        <ol>
        <li>Profile Picture</li>
        <li>Name&thinsp;/&thinsp;Pseudonym</li>
        <li>Age</li>
        <li>Gender</li>
        <li>Residence location</li>
        <li>Occupation</li>
        <li>Income</li>
        <li>Socio-economic class</li>
        <li>Interests</li>
        <li>Brands they trust</li>
        <li>Unsatisfied need&thinsp;/&thinsp;Problem</li>
        <li>Solution offered by the business</li>
        <li>Desired business goal</li>
        </ol>
        </div>
        <p>This is a good time for your business to follow your Persona's interests in social media as a reference for the content they are consuming.</p>
