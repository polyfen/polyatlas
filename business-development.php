        <h3 id="business-analysis">1.1. Business Analysis</h3>
        <small>Task</small>
        <h4>Gather information about the business</h4>
        <small>Role</small>
        <p>Marketing Analyst</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;2 hours</p>
        <small>Description</small>
        <p>The best place to start is to gather as much information as possible about the business by answering the following questions:</p>
        <div class="indent">
        <ol>
            <li>Why does the business exists?</li>
            <li>Is it a new business or are they already operational? If it's an existing business, how long has it been operating?</li>
            <li>Does the business already have a name? Are the business owners willing to change the Brand Name?</li>
            <li>What products&thinsp;/&thinsp;services does the business offer?</li>
            <li>How many people work in the business?</li>
            <li>Where is the business geographically based?</li>
            <li>How are the product&thinsp;/&thinsp;services distinctively better than those of the competition?</li>
            <li>Does the business already have a Brand Identity? An existing website or social media presence? If so, why are they wanting to Rebrand?</li>
            <li>How are the business' products&thinsp;/&thinsp;services priced when compared to the competition?</li>
            <li>What would be the business' ideal customer?</li>
            <li>Where are there customers geographically located? Is the business planning to expand to other markets?</li>
            <li>What problem do the customers have that the business can solve?</li>
        </ol>
        </div>
