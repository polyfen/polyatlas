<h3 id="social-media">4.1. Social Media</h3>
<small>Task</small>
<h4>Design profile picture</h4>
<small>Role</small>
<p>Brand Designer</p>
<small>Estimated Workload</small>
<p>0.5&thinsp;&ndash;&thinsp;1 hours</p>
<small>Description</small>
<p>Set the Ideogram on an square canvas for Screen (RGB). Considering that social media profile pictures are displayed at small sizes and amid a stream of content, it's advisable to use the distinctive, contrasting Accent Hue as background and set the Ideogram on negative (white). This should be suitable for Twitter, Facebook, Instagram, LinkedIn, and more.</p>
<p>Mind that on some cases the social media platform could crop the image into a circle. Beware that no content is left cropped out and that the Ideogram is centred well within the circle.</p>
