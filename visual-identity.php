        <h1 id="visual-identity">2. Visual Identity</h1>
        <p>The Visual Identity includes all the graphic Trademarks of the Brand, such as the Logo System, Typeface Selection, Colour Scheme, and Visual Language.</p>
        <hr class="second-separator">
        <h2 id="visual-exploration">2.1. Visual Exploration</h2>
        <p>The Visual Exploration and Discovery is the cornerstone of the Visual Identity and probably one of the hardest stages of the Brand Identity Design process.</p>
        <p>This stage involves multiple iterations and a back-and-forth with incorporating the client's feedback.</p>
        <small>Task</small>
        <h4>Survey potential typefaces for the wordmark logo</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>1&thinsp;&ndash;&thinsp;2.5 hours</p>
        <small>Description</small>
        <p>The best way to start is by setting the visual tone for the Brand with the right typeface for the logo.</p>
        <img loading="lazy" src="imgs/typefaces.webp">
        <p>Decide how you would place the Brand in a range from more <strong>organic, humanistic, hand-written</strong> to more <strong>modern, digital, mechanical.</strong> If a Brand leans towards a more organic feel, a humanistic typeface with a penmanship look-and-feel would be more suitable, perhaps a serif typeface or a sans-serif with high contrast between thick and thin strokes. On the other hand, if your Brand leans towards a more modern, digital tone, then perhaps a more angular typeface would be in order, like a geometric sans-serif or a contemporary, stylised serif typeface would be suitable.</p>
        <p>Keep in mind that some typefaces are free; However, in order to set a premium tone for the look-and-feel of the Brand Identity, it would be advisable to purchase a paid typeface license. This is a good time to discuss with the client their budget and find a typeface accordingly.</p>
        <p>Browse font distributors and foundries and list potential typefaces suitable for the Brand's Wordmark.</p>
        <div class="indent">
          <ul>
            <li><a href="http://myfonts.com" target="_blank">MyFonts</a></li>
            <li><a href="http://fontsquirrel.com" target="_blank">Font Squirrel</a></li>
            <li><a href="http://fonts.adobe.com" target="_blank">Adobe Fonts</a></li>
            <li><a href="http://fontfabric.com" target="_blank">Fontfabric</a></li>
            <li><a href="http://fontshop.com" target="_blank">FontShop</a></li>
          </ul>
        </div>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Explore multiple Ideogram drafts</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>2&thinsp;&ndash;&thinsp;12 hours</p>
        <small>Description</small>
        <p>Once the Tone for the Visual Identity is set with a type Wordmark, you can start exploring different Ideogram drafts surveying different directions. You can try abstract geometry, pictorial marks, and lettermarks.</p>
        <p>This is also a good time to try out different colour schemes on the logo drafts. However, something to consider if we're prioritising functionality for a utilitarian Brand, it's good to start with a monochrome Ideogram that works on black and white. This allows you to first focus on an optimal morphology and prioritise shape over colour.</p>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Put the selected drafts to a test</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>1&thinsp;&ndash;&thinsp;3 hours</p>
        <small>Description</small>
        <p>Select the most suitable typefaces, Ideograms, and colour schemes, and put them to a test by quickly designing the cover/above-the-fold section of the website.</p>
        <p>You can also test the different directions of the Visual Exploration on mockups.</p>
        <p>The final goal of the Visual Exploration is to narrow down the single, most suitable direction for the Logo System, Typeface Selection, and Colour Scheme.</p>
        <hr class="second-separator">
        <h2 id="logo-system">2.2. Logo System</h2>
        <small>Task</small>
        <h4>Grid and fine-tune the selected Ideogram</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>1.5&thinsp;&ndash;&thinsp;3 hours</p>
        <small>Description</small>
        <p>Construct a grid, place the selected Ideogram, and fine-tune it as necessary.</p>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Develop all the corresponding alternatives of the Logo System</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>2&thinsp;&ndash;&thinsp;4 hours</p>
        <small>Description</small>
        <p>Using the fine-tuned Ideogram, set a grid and develop all other alternatives of the Logo System including the Wordmark, Emblem, and Signature in all corresponding variations of colour and composition.</p>
        <p>The colour variations of the Logo System would include each type of Logo in Monochrome (only 1 colour) in positive (for instance, in black for use over light backgrounds) and negative (for instance, on white for use over dark backgrounds), and in Full-colour.</p>
        <p>The composition variations could include a version of the Emblem which (1) displays the Ideogram on vertically top of the Wordmark, or (2) displays the Ideogram horizontally to the left of the Ideogram.</p>
        <hr class="second-separator">
        <h2 id="typeface-selection">2.3. Typeface Selection</h2>
        <small>Task</small>
        <h4>Select a typeface for headings</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1.5 hours</p>
        <small>Description</small>
        <p>Not necessarily, but typically, the typeface selected for the Wordmark can be the Brand's primary typeface for use on headings for the website and other mediums.</p>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Select a typeface for body text</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1.5 hours</p>
        <small>Description</small>
        <p>In some cases, the body text can be set with the same typeface which is used for the Wordmark logo and headings. However, it's usually recommended to use a second typeface for body text in order to create more contrast between the headings and the body text.</p>
        <p>At this point, we must ensure that the two typefaces pair harmoniously together. The goal is to use two typefaces that are different enough so it would create a distinctive contrast, but at the same time both typefaces should complement each other with some common traits.</p>
        <p>Overall, if you've chosen a geometric sans-serif typeface for the Logo, headings, and captions; then you should choose a clean, modern serif typeface for the body text.</p>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Select a secondary type selection</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1.5 hours</p>
        <small>Description</small>
        <p>Generally, you would aim to use a premium, paid-license typeface for the Logo, headings, and body text. You can use those typefaces on the business' website and other mediums. However, not all mediums are able to use premium typefaces.</p>
        <p>For instance, the Branding team may choose to use premium typefaces on the website. However, imagine the business' employees use Google Docs for text editing. In that case, Google Docs does not allow to use custom typefaces, but you can use free typefaces available in <a href="http://fonts.google.com" target="_blank">Google Fonts</a>.</p>
        <p>In summary, you must select a secondary set of typefaces as a fallback for when premium typefaces are not available.</p>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Select a tertiary type selection</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1.5 hours</p>
        <small>Description</small>
        <p>Worst case scenario, some mediums does not allow the use of custom premium typefaces, nor free Google Fonts typefaces. For instance, emails only support "system fonts" which are readily available in all computers and devices. Namely:</p>
        <div class="indent">
          <ul>
            <li>Arial</li>
            <li>Verdana</li>
            <li>Times New Roman</li>
            <li>Calibri</li>
            <li>Courier</li>
            <li>Gill Sans</li>
            <li>Georgia</li>
            <li>Garamond</li>
            <li>Trebuche</li>
            <li>Tahoma</li>
            <li>Cambria</li>
          </ul>
        </div>
        <p>Overall, Verdana is generally the most suitable tertiary typeface.</p>
        <hr class="second-separator">
        <h2 id="colour-scheme">2.4. Colour Scheme</h2>
        <small>Task</small>
        <h4>Choose an accent colour</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1 hours</p>
        <small>Description</small>
        <p>Some Brands may choose to go with a monochrome black and white colour scheme. However, to create more contrast on the Brand Touchpoints such as the buttons on the business' website, it's suggested to pick an accent colour.</p>
        <img loading="lazy" src="imgs/accent-colours.webp">
        <p>The accent colour should be <strong>distinctive</strong> enough so that the Brand can be identified just by its colour and differentiate itself from its competitors, like Coca-Cola's red, Cadbury's purple, Facebook's blue, or Tiffany's teal.</p>
        <p>Ideally, the accent colour should have enough contrast to work well over both light backgrounds and dark backgrounds.</p>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Choose support colours</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1 hours</p>
        <small>Description</small>
        <p>Support colours can be used for backgrounds, illustrations, buttons, and other secondary elements. Like DHL's yellow, Spotify's dark grey, Twitter's dark blue, or UPS' beige.</p>
        <img loading="lazy" src="imgs/support-colours.webp">
        <hr class="second-separator">
        <h2 id="visual-language">2.5. Visual Language</h2>
        <small>Task</small>
        <h4>Decide which backgrounds you're going to have</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1.5 hours</p>
        <small>Description</small>
        <p>Will your Brand Touchpoints have plain solid backgrounds? Or is it going to have a particular pattern? In which case, you can design a custom pattern, or you can rely on web resources, like <a href="http://subtlepatterns.com" target="_blank">SubtlePatterns.com</a> or <a href="https://graphicburger.com/backgrounds/" target="_blank">GraphicBurger</a>.</p>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Define the Brand's Iconography set</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1.5 hours</p>
        <small>Description</small>
        <p>Again with the Icons, you can choose to design your own custom set of icons for use on the Brand's website and other touchpoints, or you can purchase a license for already designed icons.</p>
        <p>Icon sets can be purchase in:</p>
        <div class="indent">
          <ul>
            <li><a href="https://ui8.net/category/icons" target="_blank">UI8</a></li>
            <li><a href="https://elements.envato.com/all-items/Icons" target="_blank">Envato Elements</a></li>
            <li><a href="https://www.iconfinder.com/search/?q=assets&price=premium&type=vector">IconFinder</a></li>
            <li><a href="https://graphicburger.com/icons-set/" target="_blank">GraphicBurger</a></li>
            <li><a href="http://nounproject.com" target="_blank">Noun Project</a></li>
          </ul>
        </div>
        <p>Otherwise, if there's no budget for these assets, you can use a free set of icons, like <a href="https://fontawesome.com" target="_blank">FontAwesome</a>.</p>
        <hr class="third-separator">
        <small>Task</small>
        <h4>Define the Brand's illustration style</h4>
        <small>Role</small>
        <p>Brand Designer</p>
        <small>Estimated Workload</small>
        <p>0.5&thinsp;&ndash;&thinsp;1.5 hours</p>
        <small>Description</small>
        <p>You can either design custom illustrations, or, alternatively, you can purchase stock illustrations and edit them as needed. <a href="https://undraw.co/" target="_blank">Undraw</a> has a collection of free illustrations which you can edit as needed and use for your projects; so it does also <a href="https://www.drawkit.io" target="_blank">DrawKit</a>, or you can purchase premium illustrations on <a href="https://ui8.net/category/illustrations" target="_blank">UI8</a>, <a href="https://elements.envato.com/all-items/illustrations" target="_blank">Envato Elements</a></p>


